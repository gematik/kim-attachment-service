package de.gematik.kim.kas.configs;

import de.gematik.kim.kas.filter.BaseAuthFilter;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

@Configuration
@Slf4j
public class InterceptorConfig implements WebMvcConfigurer {

  @Autowired
  private BaseAuthFilter baseAuthFilter;

  @Override
  public void addInterceptors(InterceptorRegistry registry) {
    log.info("Auth strategy set to {}", baseAuthFilter.getAuthStrategy().getClass().getName());
    registry.addInterceptor(baseAuthFilter);
  }

}
